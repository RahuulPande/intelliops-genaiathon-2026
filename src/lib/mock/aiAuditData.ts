// ── Summary Computation ────────────────────────────────

export function getAuditSummary(period = 'Last 30 days'): AIAuditSummary {
  const entries = auditEntries;

  const byLayer: Record<string, number> = {};
  const byAction: Record<string, number> = {};
  const byModel: Record<string, number> = {};
  const byStatus: Record<string, number> = {};
  let totalTokens = 0;
  let confidenceSum = 0;
  let confidenceCount = 0;
  let reviewedCount = 0;
  let nonSuccessCount = 0;
  let complianceCount = 0;

  for (const entry of entries) {
    // Layer aggregation
    byLayer[entry.layer] = (byLayer[entry.layer] ?? 0) + 1;

    // Action aggregation
    byAction[entry.action] = (byAction[entry.action] ?? 0) + 1;

    // Model aggregation
    byModel[entry.model] = (byModel[entry.model] ?? 0) + 1;

    // Status aggregation
    byStatus[entry.status] = (byStatus[entry.status] ?? 0) + 1;

    // Token totals
    totalTokens += entry.inputTokens + entry.outputTokens;

    // Confidence average
    if (entry.confidence !== null) {
      confidenceSum += entry.confidence;
      confidenceCount += 1;
    }

    // Human review rate
    if (entry.humanReviewed) reviewedCount += 1;

    // Error rate
    if (entry.status !== 'success') nonSuccessCount += 1;

    // Compliance relevance
    if (entry.complianceTags.length > 0) complianceCount += 1;
  }

  return {
    period,
    totalAnalyses: entries.length,
    byLayer,
    byAction,
    byModel,
    byStatus,
    totalTokensUsed: totalTokens,
    avgConfidence: confidenceCount > 0 ? Math.round((confidenceSum / confidenceCount) * 1000) / 1000 : 0,
    humanReviewRate: Math.round((reviewedCount / entries.length) * 1000) / 1000,
    errorRate: Math.round((nonSuccessCount / entries.length) * 1000) / 1000,
    complianceRelevant: complianceCount,
  };
}

// ── Filtered Access ────────────────────────────────────

export function getFilteredAuditEntries(filters: {
  layer?: string;
  action?: string;
  status?: string;
  period?: string;
}): AIAuditEntry[] {
  let results = [...auditEntries];

  if (filters.layer) {
    results = results.filter((e) => e.layer === filters.layer);
  }

  if (filters.action) {
    results = results.filter((e) => e.action === filters.action);
  }

  if (filters.status) {
    results = results.filter((e) => e.status === filters.status);
  }

  if (filters.period) {
    const now = new Date('2026-04-05T10:00:00Z');
    const periodDays: Record<string, number> = {
      'last-7-days': 7,
      'last-14-days': 14,
      'last-30-days': 30,
    };
    const days = periodDays[filters.period];
    if (days !== undefined) {
      const cutoff = new Date(now.getTime() - days * 24 * 60 * 60 * 1000);
      results = results.filter((e) => new Date(e.timestamp) >= cutoff);
    }
  }

  return results;
}
